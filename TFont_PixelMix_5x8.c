static int TFont_getDataSize()
{
	return 8;
}

static void TFont_getFontData(unsigned int code, unsigned char *fontData)
{

	int i;
	for (i=0; i<8; i++) fontData[i] = pgm_read_byte(&TFont2_data[code][i]);
}

/*
	get pixel from font..

	8 bits vertical data format.

*/
static int TFont_getPixel(unsigned char *fontData, int i, int j)
{
	if (i>=TFont_getWidth()) return 0;
	if (j>=TFont_getHeight()) return 0;

	if (j<8)
	{
		return (fontData[i] & (1<<j))?1:0;
	}
	else
	{
		return (fontData[16+i] & (1<<(j-8)))?1:0;
	}
}

